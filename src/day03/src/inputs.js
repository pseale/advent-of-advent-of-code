function parse() {
  const lines = input
    .split("\n")
    .map((x) => x.trim())
    .filter((x) => x !== "");

  return lines.map((x) => {
    let line = "";
    for (let i = 0; i < 32; i++) {
      line += x;
    }

    return line;
  });
}

module.exports = function getInputs() {
  // get stuff from the outside world
  const forest = parse(input);
  const trees = [];
  for (let row = 0; row < forest.length; row++) {
    for (let col = 0; col < forest[row].length; col++) {
      if (forest[row][col] === "#") {
        trees.push({ row, col });
      }
    }
  }
  return {
    forest,
    trees,
    collisions: [{ row: 1, col: 3 }],
  };
};

